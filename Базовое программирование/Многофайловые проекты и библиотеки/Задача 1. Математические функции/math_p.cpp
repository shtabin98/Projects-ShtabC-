#include "math_p.h"
#include <cmath>

double plus(double i, double j)
{
	return i + j;
};

double minus(double i, double j)
{
	return i - j;
};

double umn(double i, double j)
{
	return i * j;
};

double delen(double i, double j)
{
	return i / j;
};

double stepen(double i, double j)
{
	return pow(i,j);
};