#include "math_p.h"
#include <cmath>

double addition(double i, double j)
{
	return i + j;
};

double subtraction(double i, double j)
{
	return i - j;
};

double multiplication(double i, double j)
{
	return i * j;
};

double division(double i, double j)
{
	return i / j;
};

double exponentiation(double i, double j)
{
	return pow(i,j);
};
